    private void button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button1ActionPerformed
        if(name_txt.getText().isEmpty() || password_txt.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Complete All Fields", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        else{
            try{
            connection=Database.connectiondb();
            pst=connection.prepareStatement("SELECT * FROM admin WHERE name = ? AND password = ?");
            pst.setString(1,name_txt.getText());
            pst.setString(2,password_txt.getText());
            rs=pst.executeQuery();
            
            if(rs.next()){
                JOptionPane.showMessageDialog(this, "Login Successfull");
                AdminDashboard frame2=new AdminDashboard();
                frame2.setVisible(true);
                frame2.setLocationRelativeTo(null);
                this.hide();
            }
            else{
                JOptionPane.showMessageDialog(this, "Login Failed");
            }
        }
        catch(Exception ex){
            ex.printStackTrace();
        }
        }
    }//GEN-LAST:event_button1ActionPerformed

